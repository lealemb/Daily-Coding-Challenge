//concat( )
//This method is used to merge two or more arrays and returns a new array, without changing the existing arrays.

const arr1 = ["a", "b", "c"]; 

const arr2 = ["d", "e", "f"];

console.log(arr1.concat(arr2)); // ["a", "b", "c", "d", "e", "f"] 
console.log(arr1); // ["a", "b", "c"]
console.log(arr2); //  ["d", "e", "f"];
